    private void buttonUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonUbahActionPerformed
        //Mengambil Nim yang akan diupdate
        int index = tableMahasiswa.getSelectedRow();
        //Pemilihan User diatas index 0
        if (index >= 0) {
            //User memilih index pertama dalam table, yaitu nim 
            String nim = tableMahasiswa.getValueAt(index, 0).toString();
            //Pemanggilan Class MahasiswaDetail
            MahasiswaDetail detail = new MahasiswaDetail(this, true, nim);
            //Menampilkan Panel MahasiswaDetail
            detail.setVisible(true); 
        }
    }//GEN-LAST:event_buttonUbahActionPerformed

