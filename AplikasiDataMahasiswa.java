    //Membuat Fungsi Update Data
    public void updateMahasiswa(pelanggan mahasiswa){
        //Mencari NIM Mahasiswa
        pelanggan mhs = cari(mahasiswa.getIdPart());
        //Pengecekan tidak sama dengan null
        if (mhs != null ) {
            listData.remove(mhs);
            //Memasukkan data baru ke list Mahasiswa
            listData.add(mahasiswa);
            //memasukkan data ke model
            model = new MahasiswaTableModel(listData);
            //memasukkan data dari model ke table mahasiswa
            tableMahasiswa.setModel(model);
            //Pemberian Notice Ubah Data
            JOptionPane.showMessageDialog(this, "Ubah Data Berhasil!");
        }
    }
    
    //Membuat Fungsi Cari
    public pelanggan cari(String nim){
        for (pelanggan m : listData) {
            if (m.getIdPart().equals(nim)) {
                //kalau ditemukan
                return m;
            }
        }
        //Kalau tidak ditemukan
        return null;
    }
    
    //Membuat Fungsi Hapus Data
    private void deleteMahasiwa(String nim){
        //Mencari nim
         pelanggan mhs = cari(nim);
         //Menghapus data
         listData.remove(mhs);
         //memasukkan data ke model
         model = new MahasiswaTableModel(listData);
         //memasukkan data dari model ke table mahasiswa
         tableMahasiswa.setModel(model);
         //Pemberian Notice Hapus Data
         JOptionPane.showMessageDialog(this, "Hapus Data Berhasil!");
    }
    
